den = int(input())

if den == 1:
    print(f'Monday')
elif den == 2:
    print(f'Tuesday')
elif den == 3:
    print(f'Wednesday')
elif den == 4:
    print(f'Thursday')
elif den == 5:
    print(f'Friday')
elif den == 6:
    print(f'Saturday')
elif den == 7:
    print(f'Sunday')
else:
    print(f'Error')